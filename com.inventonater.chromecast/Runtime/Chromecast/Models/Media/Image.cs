namespace Inventonater.Chromecast.Models.Media
{
    /// <summary>
    /// Represents an image
    /// </summary>
    public class Image
    {
        /// <summary>
        /// Gets or sets the URL
        /// </summary>
        public string Url { get; set; }
        
        /// <summary>
        /// Gets or sets the height
        /// </summary>
        public int Height { get; set; }
        
        /// <summary>
        /// Gets or sets the width
        /// </summary>
        public int Width { get; set; }
    }
}
