namespace Inventonater.Chromecast.Models.Media
{
    /// <summary>
    /// Represents the type of repeat mode
    /// </summary>
    public enum RepeatModeType
    {
        /// <summary>
        /// No repeat
        /// </summary>
        RepeatOff,
        
        /// <summary>
        /// Repeat all items
        /// </summary>
        RepeatAll,
        
        /// <summary>
        /// Repeat a single item
        /// </summary>
        RepeatSingle,
        
        /// <summary>
        /// Repeat all items and shuffle
        /// </summary>
        RepeatAllAndShuffle
    }
}
